    private static void AddBrandsForCategory(
        List<Brand> brandsToAdd,
        HashSet<string> existingBrandNames,
        (string Name, string Country, int FoundedYear, int PopularityScore)[] brandData,
        string category)
    {
        foreach (var (name, country, foundedYear, popularityScore) in brandData)
        {
            var normalizedName = NormalizeBrandName(name);
            if (!existingBrandNames.Contains(normalizedName))
            {
                brandsToAdd.Add(new Brand
                {
                    Name = name,
                    NormalizedName = normalizedName,
                    Category = category,
                    Country = country,
                    FoundedYear = foundedYear,
                    PopularityScore = popularityScore,
                    IsActive = true,
                    CreatedAtUtc = DateTime.UtcNow
                });
                existingBrandNames.Add(normalizedName);
            }
        }
    }

    private static string NormalizeBrandName(string name)
    {
        return name.ToUpperInvariant()
            .Replace(" ", "")
            .Replace("-", "")
            .Replace("_", "")
            .Replace(".", "")
            .Trim();
    }
